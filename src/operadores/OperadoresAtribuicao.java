package operadores;

public class OperadoresAtribuicao {
	public static void main(String[] args) {
		int numero = 5;
		
		//quando quiser atribuir valor a uma variavel
		numero = numero + 3;
		
		//quando quiser atribuir 1
		//numero ++;
		
		System.out.println(numero ++);
		
		System.out.println(numero);
		
		System.out.println(++numero );
		
		//valor final
		System.out.println(numero);
		
		System.out.println(-- numero);
		
		boolean variavel = true;
		variavel = !variavel;
		
		System.out.println(variavel);
		
		
		
	}
}
